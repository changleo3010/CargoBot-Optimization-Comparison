def two_opt(order, max_iter=TWO_OPT_MAX_ITER):
    """Apply deterministic 2-opt local search while keeping the depot fixed."""
    best = list(order)
    best_len = route_distance(best)
    n_route = len(best)
    improved = True
    iteration = 0

    while improved and iteration < max_iter:
        improved = False
        iteration += 1
        for i in range(1, n_route - 2):
            for k in range(i + 1, n_route - 1):
                a, b = best[i - 1], best[i]
                c, d = best[k], best[k + 1]
                old_cost = get_dist(a, b) + get_dist(c, d)
                new_cost = get_dist(a, c) + get_dist(b, d)
                if new_cost < old_cost - 1e-9:
                    best[i:k + 1] = reversed(best[i:k + 1])
                    best_len += new_cost - old_cost
                    improved = True
                    break
            if improved:
                break
    return best, float(best_len)


