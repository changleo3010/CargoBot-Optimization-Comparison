def shortest_subtour(selected_edges, nodes):
    """Return the smallest connected component in the selected 2-regular graph."""
    neighbors = {node: [] for node in nodes}
    for i, j in selected_edges:
        neighbors[i].append(j)
        neighbors[j].append(i)

    unvisited = set(nodes)
    shortest_cycle = None

    while unvisited:
        component = []
        stack = [next(iter(unvisited))]
        while stack:
            current = stack.pop()
            if current not in unvisited:
                continue
            unvisited.remove(current)
            component.append(current)
            for nbr in neighbors[current]:
                if nbr in unvisited:
                    stack.append(nbr)

        if shortest_cycle is None or len(component) < len(shortest_cycle):
            shortest_cycle = component

    return shortest_cycle


def ordered_tour_from_edges(selected_edges, nodes, depot=0):
    """Convert selected undirected tour edges into an ordered depot-start tour."""
    neighbors = {node: [] for node in nodes}
    for i, j in selected_edges:
        neighbors[i].append(j)
        neighbors[j].append(i)

    if any(len(neighbors[node]) != 2 for node in nodes):
        raise RuntimeError("The selected edges do not form a valid 2-degree TSP tour.")

    ordered = [depot]
    previous = None
    current = depot

    while True:
        candidates = [nbr for nbr in neighbors[current] if nbr != previous]
        if not candidates:
            break
        nxt = candidates[0]
        if nxt == depot:
            break
        ordered.append(nxt)
        previous, current = current, nxt

    ordered.append(depot)
    return ordered


def solve_gurobi_obstacle_aware_tsp():
    """Build and solve the Gurobi TSP MIP using obstacle-aware distances."""
    nodes = list(range(n_nodes))
    undirected_edges = list(itertools.combinations(nodes, 2))
    distance = {(i, j): get_dist(i, j) for i, j in undirected_edges}

    model = gp.Model("CargoBot_Obstacle_Aware_TSP", env=gurobi_env) #Gurobi with license
    model.Params.OutputFlag = GUROBI_TSP_OUTPUT_FLAG
    model.Params.MIPGap = GUROBI_TSP_MIP_GAP
    model.Params.LazyConstraints = 1
    if GUROBI_TSP_TIME_LIMIT_SECONDS is not None:
        model.Params.TimeLimit = GUROBI_TSP_TIME_LIMIT_SECONDS

    # Binary variables: choose whether undirected edge (i, j) belongs to the tour.
    x = model.addVars(undirected_edges, obj=distance, vtype=GRB.BINARY, name="x")

    # Degree constraints: each node must have exactly two incident selected edges.
    model.addConstrs(
        (
            gp.quicksum(x[min(i, j), max(i, j)] for j in nodes if j != i) == 2
            for i in nodes
        ),
        name="degree",
    )

    def subtour_elimination_callback(model, where):
        """Add a lazy cut whenever an integer incumbent contains a subtour."""
        if where == GRB.Callback.MIPSOL:
            values = model.cbGetSolution(model._x)
            selected = [edge for edge in model._edges if values[edge] > 0.5]
            tour = shortest_subtour(selected, model._nodes)

            if len(tour) < len(model._nodes):
                model.cbLazy(
                    gp.quicksum(
                        model._x[min(i, j), max(i, j)]
                        for i, j in itertools.combinations(tour, 2)
                    ) <= len(tour) - 1
                )

    model._x = x
    model._edges = undirected_edges
    model._nodes = nodes
    model.optimize(subtour_elimination_callback)

    if model.Status not in [GRB.OPTIMAL, GRB.TIME_LIMIT, GRB.SUBOPTIMAL]:
        raise RuntimeError(f"Gurobi did not return a usable solution. Status code: {model.Status}")

    values = model.getAttr("X", x)
    selected_edges = [edge for edge in undirected_edges if values[edge] > 0.5]
    gurobi_order = ordered_tour_from_edges(selected_edges, nodes, depot=0)
    gurobi_distance = route_distance(gurobi_order)

    result = {
        "status_code": model.Status,
        "status_name": {
            GRB.OPTIMAL: "OPTIMAL",
            GRB.TIME_LIMIT: "TIME_LIMIT",
            GRB.SUBOPTIMAL: "SUBOPTIMAL",
        }.get(model.Status, str(model.Status)),
        "objective_value": float(model.ObjVal),
        "mip_gap": float(model.MIPGap) if model.SolCount > 0 else None,
        "selected_edges": selected_edges,
        "node_order": gurobi_order,
        "distance": float(gurobi_distance),
        "full_path": stitch_node_order(gurobi_order),
    }

    model.dispose()
    return result


