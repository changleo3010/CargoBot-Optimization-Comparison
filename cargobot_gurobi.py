def interpolate_polyline(polyline, frames_per_unit=1.5):
    """Linearly interpolate a continuous polyline for smoother animation."""
    if len(polyline) <= 1:
        return list(polyline)
    out = []
    for p, q in zip(polyline[:-1], polyline[1:]):
        p_arr = np.array(p, dtype=float)
        q_arr = np.array(q, dtype=float)
        d = np.linalg.norm(q_arr - p_arr)
        n_frames = max(2, int(np.ceil(d * frames_per_unit)))
        for alpha in np.linspace(0, 1, n_frames, endpoint=False):
            out.append(tuple((1 - alpha) * p_arr + alpha * q_arr))
    out.append(tuple(polyline[-1]))
    return out


