# ============================================================
# 13. Exact weighted obstacle-aware CVRP using Gurobi MIP
# ============================================================
# This model is a CVRP extension of the official Gurobi TSP example:
# - The TSP example uses binary edge variables, degree constraints, and
#   lazy subtour elimination.
# - Here, we use binary directed arc variables x[k, i, j] for each vehicle/tour k.
# - Each cargo node is assigned to exactly one vehicle.
# - Each vehicle route starts at the depot and returns to the depot if used.
# - Each vehicle route must satisfy the capacity constraint.
# - Lazy constraints eliminate disconnected subtours that do not include the depot.
#
# Objective:
# - Minimize total obstacle-aware Dijkstra distance.
# - A fixed vehicle cost can be added if desired, but the default is zero because
#   the default model already fixes the maximum number of vehicles to the
#   theoretical minimum trip lower bound.
#
# Global optimality:
# - If status is OPTIMAL, this is globally optimal for the selected number of
#   vehicles, the fixed obstacle-aware distance matrix, and the capacity limit.
# - If a time limit is used and status is TIME_LIMIT, the result is a feasible
#   incumbent but not necessarily globally optimal.

import itertools
import subprocess
import sys

try:
    import gurobipy as gp
    from gurobipy import GRB
except ModuleNotFoundError:
    subprocess.check_call([sys.executable, "-m", "pip", "install", "gurobipy"])
    import gurobipy as gp
    from gurobipy import GRB

GUROBI_CVRP_OUTPUT_FLAG = 1
GUROBI_CVRP_MIP_GAP = 0.0
GUROBI_CVRP_TIME_LIMIT_SECONDS = None  # Use None to let Gurobi prove optimality.
GUROBI_CVRP_MAX_VEHICLES = min_trips_lower_bound
GUROBI_CVRP_FIXED_VEHICLE_COST = 0.0
GUROBI_CVRP_SYMMETRY_BREAKING = True


def find_cvrp_subtours(selected_arcs, assigned_nodes, depot=0):
    """Return disconnected directed cycles among assigned cargo nodes."""
    successor = {i: j for i, j in selected_arcs}

    # First mark all nodes reachable from the depot route.
    reachable = {depot}
    current = depot
    safety_count = 0
    while current in successor and safety_count <= len(assigned_nodes) + 2:
        nxt = successor[current]
        reachable.add(nxt)
        current = nxt
        safety_count += 1
        if current == depot:
            break

    unvisited = set(assigned_nodes) - reachable
    subtours = []

    while unvisited:
        start = next(iter(unvisited))
        cycle = []
        current = start
        while current in unvisited:
            unvisited.remove(current)
            cycle.append(current)
            current = successor.get(current, None)
            if current is None or current == depot:
                break
        if len(cycle) > 0:
            subtours.append(cycle)

    return subtours


def solve_gurobi_obstacle_aware_cvrp():
    """Solve the weighted CVRP exactly as a Gurobi MIP with lazy subtour cuts."""
    depot = 0
    nodes = list(range(n_nodes))
    cargo_nodes = list(range(1, n_nodes))
    vehicles = list(range(GUROBI_CVRP_MAX_VEHICLES))
    demands = {node: int(cargo_items[node - 1]["weight"]) for node in cargo_nodes}

    if max(demands.values()) > CAPACITY:
        raise ValueError("At least one cargo item exceeds robot capacity.")
    if GUROBI_CVRP_MAX_VEHICLES * CAPACITY < total_weight:
        raise ValueError("The selected number of vehicles is not enough for total cargo weight.")

    directed_arcs = [
        (k, i, j)
        for k in vehicles
        for i in nodes
        for j in nodes
        if i != j
    ]

    model = gp.Model("CargoBot_Obstacle_Aware_CVRP", env=gurobi_env) #Gurobi with license
    model.Params.OutputFlag = GUROBI_CVRP_OUTPUT_FLAG
    model.Params.MIPGap = GUROBI_CVRP_MIP_GAP
    model.Params.LazyConstraints = 1
    if GUROBI_CVRP_TIME_LIMIT_SECONDS is not None:
        model.Params.TimeLimit = GUROBI_CVRP_TIME_LIMIT_SECONDS

    # Binary directed arc variable: x[k, i, j] = 1 if vehicle k travels i -> j.
    x = model.addVars(directed_arcs, vtype=GRB.BINARY, name="x")

    # Binary assignment variable: z[k, i] = 1 if cargo node i is served by vehicle k.
    z = model.addVars(vehicles, cargo_nodes, vtype=GRB.BINARY, name="z")

    # Binary vehicle-use variable: y[k] = 1 if vehicle k performs a nonempty tour.
    y = model.addVars(vehicles, vtype=GRB.BINARY, name="y")

    # Each cargo is assigned to exactly one vehicle.
    model.addConstrs(
        (gp.quicksum(z[k, i] for k in vehicles) == 1 for i in cargo_nodes),
        name="assign_once",
    )

    # If a cargo is assigned to a vehicle, that vehicle must enter and leave it once.
    model.addConstrs(
        (
            gp.quicksum(x[k, i, j] for j in nodes if j != i) == z[k, i]
            for k in vehicles
            for i in cargo_nodes
        ),
        name="leave_cargo",
    )
    model.addConstrs(
        (
            gp.quicksum(x[k, j, i] for j in nodes if j != i) == z[k, i]
            for k in vehicles
            for i in cargo_nodes
        ),
        name="enter_cargo",
    )

    # A used vehicle leaves the depot once and returns to the depot once.
    model.addConstrs(
        (gp.quicksum(x[k, depot, j] for j in cargo_nodes) == y[k] for k in vehicles),
        name="leave_depot",
    )
    model.addConstrs(
        (gp.quicksum(x[k, i, depot] for i in cargo_nodes) == y[k] for k in vehicles),
        name="enter_depot",
    )

    # Capacity constraint for each vehicle/tour.
    model.addConstrs(
        (
            gp.quicksum(demands[i] * z[k, i] for i in cargo_nodes) <= CAPACITY * y[k]
            for k in vehicles
        ),
        name="capacity",
    )

    # The total weight already implies at least min_trips_lower_bound tours, but this
    # explicit bound makes the model interpretation clear.
    model.addConstr(
        gp.quicksum(y[k] for k in vehicles) >= min_trips_lower_bound,
        name="minimum_trip_lower_bound",
    )

    # Symmetry breaking: use lower-index vehicles before higher-index vehicles.
    if GUROBI_CVRP_SYMMETRY_BREAKING:
        model.addConstrs((y[k] >= y[k + 1] for k in vehicles[:-1]), name="use_order")

    # Objective: total obstacle-aware distance plus optional vehicle fixed cost.
    model.setObjective(
        gp.quicksum(get_dist(i, j) * x[k, i, j] for k, i, j in directed_arcs)
        + GUROBI_CVRP_FIXED_VEHICLE_COST * gp.quicksum(y[k] for k in vehicles),
        GRB.MINIMIZE,
    )

    def cvrp_subtour_callback(model, where):
        """Lazy callback that cuts subtours not connected to the depot."""
        if where == GRB.Callback.MIPSOL:
            x_vals = model.cbGetSolution(model._x)
            z_vals = model.cbGetSolution(model._z)
            for k in model._vehicles:
                assigned_nodes = [i for i in model._cargo_nodes if z_vals[k, i] > 0.5]
                if not assigned_nodes:
                    continue
                selected_arcs = [
                    (i, j)
                    for i in model._nodes
                    for j in model._nodes
                    if i != j and x_vals[k, i, j] > 0.5
                ]
                subtours = find_cvrp_subtours(selected_arcs, assigned_nodes, depot=model._depot)
                for subtour in subtours:
                    if len(subtour) == 0:
                        continue
                    # Directed subtour elimination cut:
                    # a closed cycle inside S can contain at most |S|-1 arcs.
                    model.cbLazy(
                        gp.quicksum(
                            model._x[k, i, j]
                            for i in subtour
                            for j in subtour
                            if i != j
                        ) <= len(subtour) - 1
                    )

    model._x = x
    model._z = z
    model._nodes = nodes
    model._cargo_nodes = cargo_nodes
    model._vehicles = vehicles
    model._depot = depot

    model.optimize(cvrp_subtour_callback)

    if model.SolCount == 0:
        raise RuntimeError(f"Gurobi did not find a feasible CVRP solution. Status code: {model.Status}")

    x_vals = model.getAttr("X", x)
    y_vals = model.getAttr("X", y)

    trip_results = []
    for k in vehicles:
        if y_vals[k] < 0.5:
            continue

        # Follow the selected arcs from the depot to reconstruct the route order.
        node_order = [depot]
        current = depot
        safety_count = 0
        while safety_count <= n_nodes + 2:
            next_candidates = [j for j in nodes if j != current and x_vals[k, current, j] > 0.5]
            if not next_candidates:
                break
            next_node = next_candidates[0]
