# - Solve the no-weight TSP exactly as a binary MIP.
# - Use the same obstacle-aware distance get_dist(i, j) from the
#   Visibility Graph + Dijkstra module.
# - The model does not compute collision-free paths directly. It only
#   chooses the visit order. The final geometric path is stitched using
#   the Dijkstra paths already stored in pair_paths.
#
# Mathematical model:
# - Binary decision variable x[i, j] = 1 if undirected edge (i, j) is
#   selected in the tour.
# - Degree constraint: every node has exactly two selected incident edges.
# - Objective: minimize the total obstacle-aware tour distance.
# - Lazy subtour elimination: whenever Gurobi finds multiple smaller
#   cycles, add a constraint to cut off the smallest subtour.
#
# Important notes:
# - This is not a built-in GurobiTSP black-box function. It is a custom
#   TSP MIP model written with gurobipy, following Gurobi's official demo.
# - If the final status is GRB.OPTIMAL, the distance is globally optimal
#   for this complete graph and this obstacle-aware distance matrix.
# - If a time limit is set and the solver stops early, the incumbent route
#   is feasible but may not be globally optimal.

