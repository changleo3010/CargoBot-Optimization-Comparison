# ============================================================
# 10. Weighted CVRP using Google OR-Tools
# ============================================================
# CVRP model:
# - One depot: routing node 0.
# - Cargo nodes: routing nodes 1..n_cargo.
# - Demand of cargo node i is manual_weights[i-1].
# - Vehicle capacity is CAPACITY.
# - Number of available vehicles is n_cargo, but a large fixed vehicle
#   cost encourages OR-Tools to use fewer trips.
