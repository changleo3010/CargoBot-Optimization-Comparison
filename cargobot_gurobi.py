"""## 9. Fixed-topology convex pickup-region heuristic

This section uses the globally optimized Gurobi CVRP result as the route order and topology baseline. Then, for each cargo, the fixed pickup point is relaxed into a small circular feasible pickup region. The route topology is fixed: all intermediate shelf-corner waypoints from the Dijkstra paths are kept fixed. Only the pickup locations are optimized.

Because the route order and intermediate corner waypoints are fixed, the refinement becomes a convex second-order cone problem:

\[
\min \sum \|q_i - v\|_2 + \|q_i-q_j\|_2
\]

subject to each optimized pickup point staying inside its pickup disk:

\[
\|q_i-p_i\|_2 \le r_i.
\]

This is a heuristic: it improves continuous pickup locations after the exact discrete CVRP route is obtained, but it is not a globally optimal TSPN/CVRP-with-neighborhoods solution.

"""

# ============================================================
# 16. Convex fixed-topology pickup-region refinement with CVXPY
# ============================================================
# Heuristic method:
# 1. Start from the Gurobi CVRP route order.
# 2. For each edge in each route, keep the Dijkstra path topology fixed.
#    This means all intermediate visibility-graph corner waypoints are fixed.
# 3. Replace each fixed cargo pickup point with a decision variable q_i.
# 4. Constrain q_i to remain inside a small circular pickup region around the
#    original reachable pickup point.
# 5. Minimize the total path length under the fixed topology.
#
# This is convex because:
# - The route order is fixed.
# - The intermediate corner waypoints are fixed.
# - The objective is a sum of Euclidean norms.
# - The pickup regions are Euclidean balls.

