PICKUP_REGION_RADIUS = 0.4
HEURISTIC_RADIUS_SHRINK_ATTEMPTS = 5
HEURISTIC_FEASIBILITY_TOL = 1e-6


def point_to_rect_distance(p, rect):
    """Return distance from point p to a closed axis-aligned rectangle."""
    x, y = p
    xmin, ymin, xmax, ymax, *_ = rect
    dx = max(xmin - x, 0.0, x - xmax)
    dy = max(ymin - y, 0.0, y - ymax)
    return float(math.hypot(dx, dy))


def compute_safe_pickup_radii(base_radius):
    """Compute conservative disk radii that stay near free-space pickup points."""
    radii = np.zeros(n_nodes, dtype=float)
    radii[0] = 0.0
    for node in range(1, n_nodes):
        p = routing_points[node]
        map_clearance = min(p[0], W - p[0], p[1], H - p[1])
        obstacle_clearance = min(point_to_rect_distance(p, rect) for rect in obstacles)
        safe_radius = min(base_radius, 0.95 * obstacle_clearance, 0.95 * map_clearance)
        radii[node] = max(0.0, safe_radius)
    return radii


def expr_point(q_var, node):
    """Return a CVXPY expression or constant point for a routing node."""
    if node == 0:
        return np.array(start_point, dtype=float)
    return q_var[node, :]


def path_template_length_expr(q_var, a, b):
    """Build the fixed-topology length expression for one route edge a -> b."""
    template = get_path(a, b)
    internal = [np.array(p, dtype=float) for p in template[1:-1]]
    pa = expr_point(q_var, a)
    pb = expr_point(q_var, b)

    if len(internal) == 0:
        return cp.norm(pa - pb, 2)

    expr = cp.norm(pa - internal[0], 2)
    for u, v in zip(internal[:-1], internal[1:]):
        expr += euclidean(u, v)
    expr += cp.norm(pb - internal[-1], 2)
    return expr


def polyline_length(polyline):
    """Return Euclidean length of a continuous polyline."""
    return float(sum(euclidean(p, q) for p, q in zip(polyline[:-1], polyline[1:])))


def build_refined_segment(a, b, refined_points):
    """Use optimized endpoints while keeping Dijkstra intermediate corners fixed."""
    template = get_path(a, b)
    start = refined_points[a]
    end = refined_points[b]
    internal = template[1:-1]
    return [start] + internal + [end]


def stitch_refined_node_order(node_order, refined_points):
    """Stitch a refined route while preserving every segment's path topology."""
    full_path = []
    for a, b in zip(node_order[:-1], node_order[1:]):
        segment = build_refined_segment(a, b, refined_points)
        if full_path:
            full_path.extend(segment[1:])
        else:
            full_path.extend(segment)
    return full_path


def refined_paths_are_visible(refined_trip_results):
    """Check whether every segment in every refined polyline avoids obstacles."""
    bad_segments = []
    for trip in refined_trip_results:
        path = trip["full_path"]
        for seg_idx, (p, q) in enumerate(zip(path[:-1], path[1:])):
            if not line_of_sight(p, q):
                bad_segments.append((trip["trip_no"], seg_idx, p, q))
    return len(bad_segments) == 0, bad_segments


def solve_fixed_topology_refinement(base_trip_results, initial_radius=PICKUP_REGION_RADIUS):
    """Solve the convex pickup-region refinement, shrinking radius if needed."""
    if "gurobi_cvrp_trip_results" not in globals():
        raise RuntimeError("Run the Gurobi CVRP cell before the refinement cell.")

    base_total_distance = float(sum(trip["distance"] for trip in base_trip_results))
    last_result = None

    for attempt in range(HEURISTIC_RADIUS_SHRINK_ATTEMPTS):
        radius = initial_radius * (0.5 ** attempt)
        safe_radii = compute_safe_pickup_radii(radius)

        q = cp.Variable((n_nodes, 2))
        constraints = [
            q[0, 0] == start_point[0],
            q[0, 1] == start_point[1],
        ]

        for node in range(1, n_nodes):
            center = np.array(routing_points[node], dtype=float)
            constraints += [
                cp.norm(q[node, :] - center, 2) <= safe_radii[node],
                q[node, 0] >= 0.0,
                q[node, 0] <= float(W),
                q[node, 1] >= 0.0,
                q[node, 1] <= float(H),
            ]

        objective_expr = 0
        for trip in base_trip_results:
            order = trip["node_order"]
            for a, b in zip(order[:-1], order[1:]):
                objective_expr += path_template_length_expr(q, a, b)

        problem = cp.Problem(cp.Minimize(objective_expr), constraints)

        installed = cp.installed_solvers()
        preferred_solvers = ["CLARABEL", "ECOS", "SCS"]
        solver_used = None
        for solver_name in preferred_solvers:
            if solver_name in installed:
                problem.solve(solver=solver_name, verbose=False)
                solver_used = solver_name
                break
        if solver_used is None:
            problem.solve(verbose=False)
            solver_used = "cvxpy_default"

        if q.value is None:
            last_result = {
                "status": problem.status,
                "solver_used": solver_used,
                "base_radius": radius,
            }
            continue

        refined_points = [tuple(map(float, p)) for p in routing_points]
        for node in range(1, n_nodes):
            refined_points[node] = (float(q.value[node, 0]), float(q.value[node, 1]))
        refined_points[0] = start_point

        refined_trip_results = []
        for trip in base_trip_results:
            refined_full_path = stitch_refined_node_order(trip["node_order"], refined_points)
            refined_distance = polyline_length(refined_full_path)
            refined_trip_results.append({
                **trip,
                "original_full_path": trip["full_path"],
                "full_path": refined_full_path,
                "original_distance": trip["distance"],
                "distance": refined_distance,
                "refined_distance": refined_distance,
            })

        feasible, bad_segments = refined_paths_are_visible(refined_trip_results)
        refined_total_distance = float(sum(trip["distance"] for trip in refined_trip_results))

        result = {
            "status": problem.status,
            "solver_used": solver_used,
            "base_radius": radius,
            "safe_radii": safe_radii,
            "refined_points": refined_points,
            "trip_results": refined_trip_results,
            "base_total_distance": base_total_distance,
            "refined_total_distance": refined_total_distance,
            "improvement": base_total_distance - refined_total_distance,
            "improvement_percent": 100.0 * (base_total_distance - refined_total_distance) / base_total_distance,
            "all_segments_visible": feasible,
            "bad_segments": bad_segments,
        }
        last_result = result

        # Stop if the refined fixed-topology paths are still collision-free.
        if feasible:
            return result

    return last_result


