CVRP_COST_SCALE = 1000
CVRP_TIME_LIMIT_SECONDS = 20
CVRP_VEHICLE_FIXED_COST = 1_000_000


def build_distance_cost_matrix(scale=CVRP_COST_SCALE):
    """Build an integer distance matrix for OR-Tools."""
    return [
        [int(round(get_dist(i, j) * scale)) for j in range(n_nodes)]
        for i in range(n_nodes)
    ]


def build_full_path_from_node_order(node_order):
    """Stitch all visibility-graph subpaths for a CVRP route."""
    return stitch_node_order(node_order)


def solve_distance_cvrp():
    """Solve the weighted CVRP with obstacle-aware distance costs."""
    depot = 0
    num_vehicles = n_cargo
    demands = [0] + [item["weight"] for item in cargo_items]
    vehicle_capacities = [CAPACITY] * num_vehicles
    distance_cost_matrix = build_distance_cost_matrix(CVRP_COST_SCALE)

    if max(demands) > CAPACITY:
        raise ValueError("At least one cargo item exceeds robot capacity.")

    manager = pywrapcp.RoutingIndexManager(n_nodes, num_vehicles, depot)
    routing = pywrapcp.RoutingModel(manager)

    def distance_callback(from_index, to_index):
        from_node = manager.IndexToNode(from_index)
        to_node = manager.IndexToNode(to_index)
        return int(distance_cost_matrix[from_node][to_node])

    transit_callback_index = routing.RegisterTransitCallback(distance_callback)
    routing.SetArcCostEvaluatorOfAllVehicles(transit_callback_index)

    def demand_callback(from_index):
        from_node = manager.IndexToNode(from_index)
        return int(demands[from_node])

    demand_callback_index = routing.RegisterUnaryTransitCallback(demand_callback)
    routing.AddDimensionWithVehicleCapacity(
        demand_callback_index,
        0,
        vehicle_capacities,
        True,
        "Capacity",
    )

    # Penalize every used vehicle so the solver prefers fewer trips.
    for vehicle_id in range(num_vehicles):
        routing.SetFixedCostOfVehicle(CVRP_VEHICLE_FIXED_COST, vehicle_id)

    search_parameters = pywrapcp.DefaultRoutingSearchParameters()
    search_parameters.first_solution_strategy = (
        routing_enums_pb2.FirstSolutionStrategy.PATH_CHEAPEST_ARC
    )
    search_parameters.local_search_metaheuristic = (
        routing_enums_pb2.LocalSearchMetaheuristic.GUIDED_LOCAL_SEARCH
    )
    search_parameters.time_limit.FromSeconds(CVRP_TIME_LIMIT_SECONDS)

    solution = routing.SolveWithParameters(search_parameters)
    if solution is None:
        raise RuntimeError("OR-Tools did not find a feasible CVRP solution.")

    trip_results = []
    for vehicle_id in range(num_vehicles):
        index = routing.Start(vehicle_id)
        node_order = []

        while not routing.IsEnd(index):
            node_order.append(manager.IndexToNode(index))
            index = solution.Value(routing.NextVar(index))
        node_order.append(manager.IndexToNode(index))

        # Skip unused vehicles that only travel depot -> depot.
        if len(node_order) <= 2 and node_order[0] == 0 and node_order[-1] == 0:
            continue

        group = [node - 1 for node in node_order if node != 0]
        load = int(sum(cargo_items[idx]["weight"] for idx in group))
        distance = route_distance(node_order)
        full_path = build_full_path_from_node_order(node_order)

        trip_results.append({
            "trip_no": len(trip_results),
            "vehicle_id": vehicle_id,
            "node_order": node_order,
            "group": group,
            "load": load,
            "distance": distance,
            "full_path": full_path,
        })

    # Validate assignment and capacity constraints.
    assigned = [idx for trip in trip_results for idx in trip["group"]]
    assert sorted(assigned) == list(range(n_cargo)), "Every cargo must be assigned exactly once."
    assert all(trip["load"] <= CAPACITY for trip in trip_results), "Every trip must satisfy capacity."

    route_rows = []
    for trip in trip_results:
        cargo_ids = trip["group"]
        cargo_weights = [cargo_items[idx]["weight"] for idx in cargo_ids]
        route_rows.append({
            "trip": trip["trip_no"] + 1,
            "vehicle_id": trip["vehicle_id"],
            "load_kg": trip["load"],
            "num_items": len(cargo_ids),
            "distance": round(trip["distance"], 2),
            "cargo_visit_order": cargo_ids,
            "cargo_weights_in_order": cargo_weights,
            "node_order": trip["node_order"],
        })

    route_df = pd.DataFrame(route_rows)
    total_distance = float(sum(trip["distance"] for trip in trip_results))

    return {
        "trip_results": trip_results,
        "route_df": route_df,
        "total_distance": total_distance,
        "n_trips": len(trip_results),
        "objective_value": int(solution.ObjectiveValue()),
    }


cvrp_solution = solve_distance_cvrp()
